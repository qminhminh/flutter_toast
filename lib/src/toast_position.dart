/// Vị trí hiển thị toast trên màn hình
enum ToastPosition {
  /// Hiển thị ở trên cùng (giữa)
  top,

  /// Hiển thị ở giữa
  center,

  /// Hiển thị ở dưới cùng (giữa)
  bottom,

  /// Hiển thị ở góc trên bên trái
  topLeft,

  /// Hiển thị ở góc trên bên phải
  topRight,

  /// Hiển thị ở góc dưới bên trái
  bottomLeft,

  /// Hiển thị ở góc dưới bên phải
  bottomRight,
}
